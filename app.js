let selectedCategory = "";
let selectedTotalQuestions = 0;
let selectedAccuracy = 0;
let currentQuestionIndex = 0;
let currentTurn = 1;
let isTwoPlayerMode = false;
let activeQuestionsList = [];


let singleScores = { A: 0, B: 0, C: 0, D: 0 };
let turn1Scores = { A: 0, B: 0, C: 0, D: 0 };
let turn2Scores = { A: 0, B: 0, C: 0, D: 0 };


function goToLevel(category) {
    selectedCategory = category;
    switchScreen('step-category', 'step-level');
}

function goBackToCategory() {
    switchScreen('step-level', 'step-category');
    switchScreen('step-quiz', 'step-category');
}

function switchScreen(hideId, showId) {
    document.getElementById(hideId).classList.add('hidden');
    document.getElementById(showId).classList.remove('hidden');
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

function startQuiz(totalQuestions, accuracy) {
    selectedTotalQuestions = totalQuestions;
    selectedAccuracy = accuracy;
    currentQuestionIndex = 0;
    currentTurn = 1; 
    

    singleScores = { A: 0, B: 0, C: 0, D: 0 };
    turn1Scores = { A: 0, B: 0, C: 0, D: 0 };
    turn2Scores = { A: 0, B: 0, C: 0, D: 0 };

    isTwoPlayerMode = ['love_couple', 'best_friend', 'crush', 'family'].includes(selectedCategory);

    const rawQuestions = quizData[selectedCategory] || quizData['personality'];
    activeQuestionsList = rawQuestions.slice(0, selectedTotalQuestions);

    const categoryNames = {
        personality: "ຕົວເອງ ນິໄສໃຈຄໍ",
        love_couple: "ບົດທົດສອບຄູ່ຮັກ (2 ຝ່າຍ)",
        crush: "ໂຕເອງ ກັບ ຄົນລົມ (2 ຝ່າຍ)",
        best_friend: "ບົດທົດສອບໝູ່ຮັກ / ຄູ່ຫູ (2 ຝ່າຍ)",
        family: "ໂຕເອງ ກັບ ຍາດພີ່ນ້ອງ (2 ຝ່າຍ)",
        society: "ຕົວເອງ ໃນສັງຄົມ"
    };
    
    document.getElementById('quiz-category-badge').innerText = categoryNames[selectedCategory] || "ບົດທົດສອບ";

    if (isTwoPlayerMode) {
        document.getElementById('turn-indicator-box').classList.remove('hidden');
    } else {
        document.getElementById('turn-indicator-box').classList.add('hidden');
    }

    switchScreen('step-level', 'step-quiz');
    showQuestion();
}

function showQuestion() {
    if (currentQuestionIndex >= activeQuestionsList.length) {
        showResult();
        return;
    }

    const currentQuestion = activeQuestionsList[currentQuestionIndex];
    document.getElementById('quiz-progress-text').innerText = `ຄຳຖາມຂໍ້ທີ: ${currentQuestionIndex + 1}/${activeQuestionsList.length}`;
    document.getElementById('quiz-progress-bar').style.width = `${(currentQuestionIndex / activeQuestionsList.length) * 100}%`;
    document.getElementById('question-text').innerText = currentQuestion.q;

    const choicesContainer = document.getElementById('choices-container');
    choicesContainer.innerHTML = ""; 

    if (isTwoPlayerMode) {
        const indicator = document.getElementById('turn-indicator');
        if (currentTurn === 1) {
            indicator.innerText = (selectedCategory === 'love_couple') ? "🙋‍♂️ ຕາຝ່າຍຊາຍ ຕອບ" : "🤝 ຕາຝ່າຍ A ຕອບ";
            indicator.className = "badge fs-6 px-4 py-2 shadow-sm rounded-pill " + (selectedCategory === 'love_couple' ? "bg-primary" : "text-white");
            if(selectedCategory !== 'love_couple') indicator.style.backgroundColor = "#6366f1";
            
            choicesContainer.innerHTML = createChoicesHTML(currentQuestion.turn1_answers, 'submitAnswerTwoPlayer');
        } else {
            indicator.innerText = (selectedCategory === 'love_couple') ? "🙋‍♀️ ຕາຝ່າຍຍິງ ຕອບ" : "🤝 ຕາຝ່າຍ B ຕອບ";
            indicator.className = "badge fs-6 px-4 py-2 shadow-sm rounded-pill " + (selectedCategory === 'love_couple' ? "text-white" : "bg-success");
            if(selectedCategory === 'love_couple') indicator.style.backgroundColor = "#db2777";
            
            choicesContainer.innerHTML = createChoicesHTML(currentQuestion.turn2_answers, 'submitAnswerTwoPlayer');
        }
    } else {
        choicesContainer.innerHTML = createChoicesHTML(currentQuestion, 'submitSingleAnswer');
    }
}
function createChoicesHTML(data, funcName) {
    return `
        <div class="col-sm-6"><div class="quiz-choice-card shadow-sm" onclick="${funcName}('A')"><div class="choice-letter letter-A">A</div><div class="choice-text">${data.a}</div></div></div>
        <div class="col-sm-6"><div class="quiz-choice-card shadow-sm" onclick="${funcName}('B')"><div class="choice-letter letter-B">B</div><div class="choice-text">${data.b}</div></div></div>
        <div class="col-sm-6"><div class="quiz-choice-card shadow-sm" onclick="${funcName}('C')"><div class="choice-letter letter-C">C</div><div class="choice-text">${data.c}</div></div></div>
        <div class="col-sm-6"><div class="quiz-choice-card shadow-sm" onclick="${funcName}('D')"><div class="choice-letter letter-D">D</div><div class="choice-text">${data.d}</div></div></div>
    `;
}

function submitSingleAnswer(choice) {
    singleScores[choice]++;
    currentQuestionIndex++;
    fadeTransition();
}

function submitAnswerTwoPlayer(choice) {
    if (currentTurn === 1) {
        turn1Scores[choice]++;
        currentTurn = 2; 
    } else {
        turn2Scores[choice]++;
        currentTurn = 1; 
        currentQuestionIndex++; 
    }
    fadeTransition();
}

function fadeTransition() {
    const quizBox = document.getElementById('step-quiz');
    quizBox.style.opacity = 0;
    setTimeout(() => {
        showQuestion();
        quizBox.style.opacity = 1;
    }, 200);
}

function showResult() {
    document.getElementById('quiz-progress-bar').style.width = `100%`;
    document.getElementById('result-accuracy-text').innerText = `ລະດັບຄວາມແມ່ນຍຳຂອງຜົນວິເຄາະ: ${selectedAccuracy}%`;

    let finalAnalysis = isTwoPlayerMode 
        ? resultTemplates.couple[selectedCategory] 
        : resultTemplates.single[`high${Object.keys(singleScores).reduce((a, b) => singleScores[a] > singleScores[b] ? a : b)}`];

    if (selectedAccuracy === 50) {
        finalAnalysis = finalAnalysis.substring(0, 110) + "... (ປົດລັອກລະດັບ 2 ຫຼື 3 ເພື່ອຮັບບົດວິເຄາະລົງເລິກ)";
    }

    document.getElementById('result-analysis-text').innerHTML = finalAnalysis;
    switchScreen('step-quiz', 'step-result');
}

